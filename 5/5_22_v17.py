import math as m


def main(y, x, z):
    n = len(y)
    result = 0
    for i in range(1, n+1):
        result += m.exp(91*z[m.ceil(i/3)-1]**3+41 *
                        y[m.ceil(i/4)-1]**2+4*x[i-1])**2/65
    return result


