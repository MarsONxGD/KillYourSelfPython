def main(x):
    n = len(x)
    r = 0
    for i in range(0, n):
        r += x[n-1-i]+1+x[n-1-i]**2
    return r


print(main([0.17, -0.03, 0.24, -0.17, -0.43, 0.55, -0.42]))   # 7.69e+00
