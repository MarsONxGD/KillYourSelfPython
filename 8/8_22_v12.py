def main(x):
    m1 = x & 0b11111111
    m2 = (x >> 8) & 0b1
    m3 = (x >> 9) & 0b1
    m4 = (x >> 10) & 0b1111111111
    m5 = (x >> 20) & 0b1111111
    m6 = (x >> 27) & 0b1111
    return (m5 << 24) + (m1 << 16) + (m3 << 15) + (m6 << 11) + (m2 << 10) + m4


print(main(937030124))   # 2112632444
print(main(911473320))   # 1705554174
print(main(1022333326))  # 1317945316
print(main(389881587))   # 1945342791
