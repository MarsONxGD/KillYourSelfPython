def main(x):
    x21 = {'A':0,'O':1,'U':2}
    x31 = {'Z':8,'L':9}
    x32 = {'Z':x21[x[2]],'L':3}
    x41 = {2011:4,2017:5}
    x42 = {2011:7,2017:x31[x[3]]}
    x11 = {1963:x32[x[3]],1959:x41[x[4]],1965:6}
    x22 = {'A':x42[x[4]],'O':10,'U':11}
    init = {1960:x11[x[1]],1977:x22[x[2]]}
    return init[x[0]]

print(main([1960, 1965, 'O', 'Z', 2017]))
print(main([1977, 1959, 'A', 'L', 2017]))
